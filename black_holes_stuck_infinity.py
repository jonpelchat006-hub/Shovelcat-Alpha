@dataclass
class BlackHole:
    """
    A black hole as stuck infinity.
    """
    mass: float  # The infinity content (large number representing ∞)
    keys_stored: int = 0  # Information content
    
    def get_speed(self) -> float:
        """Speed approaches 0 as mass approaches infinity."""
        return C / math.sqrt(1 + self.mass)
    
    def get_theta(self) -> float:
        """θ approaches 0 as mass approaches infinity."""
        return PI/4 / math.sqrt(1 + self.mass)
    
    def get_exponent(self) -> float:
        """The effective exponent (should be ∞ but stuck at ~1)."""
        # As mass → ∞, exponent stays at 1 (not growing)
        return 1.0 + 0.001 * (1 / (1 + self.mass))
    
    def is_god_compatible(self) -> bool:
        """Check if compatible with God (needs ∞^∞)."""
        # Would need exponent → ∞, but we're stuck at ~1
        return self.get_exponent() > 10  # Never true for large mass
    
    def emit_hawking_particle(self) -> Tuple[float, int]:
        """
        Emit a Hawking particle (mass piece + key).
        Returns (mass_emitted, keys_emitted).
        """
        if self.mass <= 0:
            return (0, 0)
        
        # Smaller black holes emit faster (higher temperature)
        emission_rate = 1 / (self.mass + 1)
        mass_emitted = emission_rate * 0.01
        keys_emitted = 1 if self.keys_stored > 0 else 0
        
        self.mass -= mass_emitted
        self.keys_stored -= keys_emitted
        
        return (mass_emitted, keys_emitted)
    
    def absorb_matter(self, mass: float, keys: int = 1) -> None:
        """Absorb matter (increasing mass and storing keys)."""
        self.mass += mass
        self.keys_stored += keys


